package com.pe.places.retrofit.response.typicode;

public class GeoResponse {
    private String lat;
    private String lng;

    public GeoResponse() {

    }

    public String getLat() {
        return lat;
    }

    public void setLat(String lat) {
        this.lat = lat;
    }

    public String getLng() {
        return lng;
    }

    public void setLng(String lng) {
        this.lng = lng;
    }
}
